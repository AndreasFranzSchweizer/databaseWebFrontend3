-- Erstellen der Tabelle Kunden
CREATE TABLE Kunden (
    Kunde_ID INTEGER PRIMARY KEY,
    Name VARCHAR(100),
    Stadt VARCHAR(50),
    Land VARCHAR(50)
);

-- Erstellen der Tabelle Produkte
CREATE TABLE Produkte (
    Produkt_ID INTEGER PRIMARY KEY,
    Produktname VARCHAR(100),
    Kategorie VARCHAR(50),
    Preis NUMERIC(10, 2),
    Lagerbestand INTEGER
);

-- Erstellen der Tabelle Bestellungen
CREATE TABLE Bestellungen (
    Bestell_ID INTEGER PRIMARY KEY,
    Kunde_ID INTEGER,
    Bestelldatum DATE,
    Produkt_ID INTEGER,
    Anzahl INTEGER,
    Gesamtpreis NUMERIC(10, 2),
    FOREIGN KEY (Kunde_ID) REFERENCES Kunden(Kunde_ID),
    FOREIGN KEY (Produkt_ID) REFERENCES Produkte(Produkt_ID)
);

